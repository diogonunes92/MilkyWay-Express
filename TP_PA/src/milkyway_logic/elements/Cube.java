package milkyway_logic.elements;

public class Cube {

    private final int id;
    private String color;
    private String type;

    public Cube(int id) {
        this.id = id;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
